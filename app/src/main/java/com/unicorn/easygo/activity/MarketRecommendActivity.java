    private void doSearchQuery(Location location) {
        query = new PoiSearch.Query("", "060400", "027");
        query.setPageSize(20);
        query.setPageNum(1);
        poiSearch = new PoiSearch(this, query);
        poiSearch.setOnPoiSearchListener(this);
        poiSearch.setBound(new PoiSearch.SearchBound(
                new LatLonPoint(location.getLongitude(),location.getLatitude()),
                2000));
        poiSearch.searchPOIAsyn();
    }

    @Override
    public void onPoiSearched(PoiResult result, int rcode) {
        //Log.i("poiResult", poiResult.toString());
        if (rcode == AMapException.CODE_AMAP_SUCCESS) {
            if (result != null && result.getQuery() != null) {// 搜索poi的结果
                if (result.getQuery().equals(query)) {// 是否是同一条
                    poiResult = result;
                    poiItems = poiResult.getPois();// 取得第一页的poiitem数据，页数从数字0开始
                    List<SuggestionCity> suggestionCities = poiResult
                            .getSearchSuggestionCitys();// 当搜索不到poiitem数据时，会返回含有搜索关键字的城市信息
                    if (poiItems != null && poiItems.size() > 0) {
                        //并还原点击marker样式
                        if (mlastMarker != null) {
                            //resetlastmarker();
                        }
                        //清理之前搜索结果的marker
                        if (poiOverlay !=null) {
                            //poiOverlay.removeFromMap();
                        }
                        //aMap.clear();
                        poiOverlay = new myPoiOverlay(aMap, poiItems);
                        poiOverlay.addToMap();
                        poiOverlay.zoomToSpan();

                        recyclerView.setAdapter(new PoiItemAdapter(this, poiItems));

                    } else if (suggestionCities != null
                            && suggestionCities.size() > 0) {
                        showSuggestCity(suggestionCities);
                    } else {
                        ToastUtil.show(MarketRecommendActivity.this,
                                R.string.no_result);
                    }
                }
            } else {
                ToastUtil.show(MarketRecommendActivity.this, R.string.no_result);
            }
        } else  {
            ToastUtil.showerror(this.getApplicationContext(), rcode);
        }
    }

